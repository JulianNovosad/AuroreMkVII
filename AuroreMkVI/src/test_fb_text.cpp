struct Framebuffer {
    int fd;
    void* data;
    int width;
    int height;
    int pitch;
    int bpp;
};

bool init_fb(Framebuffer& fb) {
    fb.fd = open("/dev/fb0", O_RDWR);
    if (fb.fd < 0) {
        std::cerr << "Failed to open framebuffer" << std::endl;
        return false;
    }

    struct fb_var_screeninfo vinfo;
    struct fb_fix_screeninfo finfo;

    if (ioctl(fb.fd, FBIOGET_VSCREENINFO, &vinfo) < 0) {
        std::cerr << "Failed to get vinfo" << std::endl;
        return false;
    }

    if (ioctl(fb.fd, FBIOGET_FSCREENINFO, &finfo) < 0) {
        std::cerr << "Failed to get finfo" << std::endl;
        return false;
    }

    fb.width = vinfo.xres;
    fb.height = vinfo.yres;
    fb.bpp = vinfo.bits_per_pixel;
    fb.pitch = finfo.line_length;

    fb.data = mmap(0, finfo.smem_len, PROT_READ | PROT_WRITE, MAP_SHARED, fb.fd, 0);
    if (fb.data == MAP_FAILED) {
        std::cerr << "Failed to mmap framebuffer" << std::endl;
        return false;
    }

    std::cout << "Framebuffer: " << fb.width << "x" << fb.height << " @ " << fb.bpp << "bpp" << std::endl;
    return true;
}

void draw_pixel(Framebuffer& fb, int x, int y, uint32_t color) {
    if (x < 0 || x >= fb.width || y < 0 || y >= fb.height) return;

    if (fb.bpp == 16) {
        uint16_t r = (color >> 16) & 0xFF;
        uint16_t g = (color >> 8) & 0xFF;
        uint16_t b = color & 0xFF;
        uint16_t pixel = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
        uint16_t* ptr = (uint16_t*)((uint8_t*)fb.data + y * fb.pitch + x * 2);
        *ptr = pixel;
    } else {
        uint32_t* ptr = (uint32_t*)((uint8_t*)fb.data + y * fb.pitch + x * (fb.bpp / 8));
        *ptr = color;
    }
}

