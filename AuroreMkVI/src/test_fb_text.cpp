int main() {
    std::cout << "=== Framebuffer Text Test ===" << std::endl;

    Framebuffer fb;
    if (!init_fb(fb)) {
        std::cerr << "Framebuffer init failed" << std::endl;
        return 1;
    }

    memset(fb.data, 0, fb.height * fb.pitch);

    uint32_t red = 0x0000FF;
    uint32_t green = 0x00FF00;
    uint32_t yellow = 0x00FFFF;
    uint32_t white = 0xFFFFFF;

    int y = 80;
    const char* numbers = "0123456789";
    for (int i = 0; numbers[i]; i++) {
        draw_char_8x8(fb, 50 + i * 35, y, numbers[i], red, 3);
    }

    y += 40;
    const char* alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
    for (int i = 0; alphabet[i]; i++) {
        draw_char_8x8(fb, 20 + i * 22, y, alphabet[i], green, 3);
    }

    y += 40;
    const char* msg = "ABC 123";
    for (int i = 0; msg[i]; i++) {
        char c = msg[i];
        if (c == ' ') continue;
        draw_char_8x8(fb, 50 + i * 35, y, c, white, 3);
    }

    y += 40;
    const char* msg2 = "HELLO";
    for (int i = 0; msg2[i]; i++) {
        draw_char_8x8(fb, 50 + i * 35, y, msg2[i], yellow, 3);
    }

    std::cout << "Text displayed for 10 seconds..." << std::endl;
    std::cout << "Row 1 (RED): Numbers 0-9" << std::endl;
    std::cout << "Row 2 (GREEN): Alphabet A-Z" << std::endl;
    std::cout << "Row 3 (WHITE): ABC 123" << std::endl;
    std::cout << "Row 4 (YELLOW): HELLO" << std::endl;
    std::cout << "If you see this text on screen, framebuffer text works!" << std::endl;

    sleep(10);

    munmap(fb.data, fb.height * fb.pitch);
    close(fb.fd);

    return 0;
}
